    for (Section section in sections) {
      for (Account account in section.accounts) {
        if (account.areaName == null) {
          //  print(account.areaName);
          continue;
        }
        Area area = areaByName[account.areaName.trim()];
        area.accounts.add(account);
      }
    }

    return returnAreas;
  }

